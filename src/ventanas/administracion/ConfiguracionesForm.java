    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (noValido()) {
            return;
        } 
        
        this.configuration.setCode(this.codigoTxt.getText());
        this.configuration.setName(this.nombreTxt.getText());
        this.configuration.setValue(this.valorTxt.getText());
        if (this.configuration.getId() == null) { 
                configuraciones.crear(this.configuration);
                this.dispose(); 
//                JOptionPane.showMessageDialog(this, "No se puede grabar el registro", "ERROR", JOptionPane.ERROR_MESSAGE); 
        } else {
            configuraciones.actualizar(this.configuration);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private boolean noValido() {
        boolean error = false;
        StringBuilder mensaje = new StringBuilder();
        String name = this.nombreTxt.getText();
        String code = this.codigoTxt.getText();
        String value = this.valorTxt.getText();
    
        if(name.isEmpty()){
            error=true;
             mensaje.append("- Campo Nombre es  obligatorio \n");
        }
        
        if(value.isEmpty()){
            error=true;
             mensaje.append("- Campo Valor  es  obligatorio \n");
        }
        
        
        if(code.isEmpty()){
            error=true;
             mensaje.append("- Campo Código  es  obligatorio \n");
        } 
        
        if(!mensaje.toString().isEmpty()){
            JOptionPane.showMessageDialog(this, mensaje, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        
        return error;
    }

